package com.linear.model;

public class RightHandSide {

	private String op;
	private String lhs;
	private int rhs;

	public String getOp() {
		return op;
	}

	public void setOp(String op) {
		this.op = op;
	}

	public String getLhs() {
		return lhs;
	}

	public void setLhs(String lhs) {
		this.lhs = lhs;
	}

	public int getRhs() {
		return rhs;
	}

	public void setRhs(int rhs) {
		this.rhs = rhs;
	}
}
